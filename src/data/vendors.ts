export const getVendorById = (id: string) => {
  const vendor = vendors.find((v) => v.id === id || v.slug === id);
  // Add slug if it doesn't exist
  if (vendor && !vendor.slug) {
    vendor.slug = vendor.id;
  }
  return vendor;
};

export const getVendorsByCategory = (category: string) => {
  const fullCategory = slugToCategory[category] || category;
  return vendors.filter((v) => v.categories.includes(fullCategory)).map(v => ({
    ...v,
    slug: v.slug || v.id
  }));
};

export const searchVendors = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return vendors.filter(
    (v) =>
      v.name.toLowerCase().includes(lowercaseQuery) ||
      v.description.toLowerCase().includes(lowercaseQuery) ||
      v.tagline.toLowerCase().includes(lowercaseQuery) ||
      v.categories.some((cat) => cat.toLowerCase().includes(lowercaseQuery))
  ).map(v => ({
    ...v,
    slug: v.slug || v.id
  }));
};

